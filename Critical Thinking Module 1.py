user_numbers = input("Input two numbers, separated by a space: ")
numbers = user_numbers.split()
num1 = int(numbers[0])
num2 = int(numbers[1])

print("The sum of these numbers is ", num1 + num2)
print("The difference of these numbers is ", num1 - num2)
print("The product of these numbers is ", num1 * num2)
print("The quotient of these numbers is ", num1 / num2)
